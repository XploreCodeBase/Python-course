# Zmienna z nazwą projektu
project_name = "Analiza sprzedaży"

# Zmienna z autorem raportu
author = "Alicja"

# Zmienna z wersją raportu
version = 1.0

# Wyświetlamy raport
# \n tworzy nową linię
# \t dodaje tabulator (wyrównanie)
print("Raport projektu:", project_name)
print("Autor:\t\t", author)
print("Wersja:\t\t", version)

# Dodajemy puste linie i końcowe podziękowanie
# Wszystko w jednym wywołaniu print()
print("\nDziękujemy za skorzystanie z raportu.\nZapraszamy ponownie.\nMiłego dnia!")

